// wrapped

func bottomWrap(err error) error {
	return cluerr.Wrap(err, "bottom wrap")
}

func midWrap(err error) error {
	return cluerr.Wrap(bottomWrap(err), "mid wrap")
}

func topWrap(err error) error {
	return cluerr.Wrap(midWrap(err), "top wrap")
}

