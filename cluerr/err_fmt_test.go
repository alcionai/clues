// requires sets of 3 strings
func commentRE(ss ...string) string {
	result := ""

	for i := 0; i < len(ss); i += 3 {
		result += ss[i] + " - "
		result += ss[i+1] + `:\d+\n`
		result += `\t` + ss[i+2]

		if len(ss) > i+3 {
			result += `\n`
		}
	}

	return result
}

func commentMatches(
	t *testing.T,
	expect, result string,
) {
	re := regexp.MustCompile(expect)

	if !re.MatchString(result) {
		t.Errorf(
			"unexpected comments stack"+
				"\n\nexpected (raw)\n\"%s\""+
				"\n\ngot (raw)\n%#v"+
				"\n\ngot (fmt)\n\"%s\"",
			re, result, result)
	}
}
